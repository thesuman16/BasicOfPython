# Weather Activity Suggestion
# Suggest an activity based on the weather, ex- Sunny = go for a walk, Rainy = Read  a book, 
# Snowy = build a snowman

weather = "Sunny"

if weather == "Sunny":
    activity = "Go for a Walk"
elif weather == "Rainy":
    activity = "Read a book"
elif weather == "Snowy":
    activity = "Build a Snowman"
    
print(activity)